const BackendFAQ: React.FC = () => {
  const [openIndex, setOpenIndex] = useState<number | null>(null);

  const toggle = (i: number) => {
    setOpenIndex(openIndex === i ? null : i);
  };

  return (
    <div className="backend-faq">
      <div className="bfaq-hero">
        <div className="bfaq-hero-icon">
          <HelpCircle size={40} />
        </div>
        <h1 className="bfaq-title">Часті питання</h1>
        <p className="bfaq-subtitle">
          Бекенд і бази даних — відповідаємо на найпоширеніші запитання
        </p>
      </div>

      <div className="bfaq-content">
        <div className="bfaq-list">
          {faqs.map((faq, i) => (
            <div
              key={i}
              className={`bfaq-item ${openIndex === i ? "bfaq-open" : ""}`}
            >
              <button className="bfaq-question" onClick={() => toggle(i)}>
                <span>{faq.question}</span>
                {openIndex === i ? (
                  <ChevronUp size={18} />
                ) : (
                  <ChevronDown size={18} />
                )}
              </button>
              {openIndex === i && (
                <div className="bfaq-answer">
                  <p>{faq.answer}</p>
                </div>
              )}
            </div>
          ))}
        </div>

        <div className="bfaq-footer">
          <p>Не знайшли відповіді на своє питання?</p>
          <span className="bfaq-footer-hint">
            💬 Напишіть нам у чат — відповімо швидко і зрозуміло
          </span>
        </div>
      </div>
    </div>
  );
};

export default BackendFAQ;
